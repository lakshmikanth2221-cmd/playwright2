const { chromium } = require('playwright');
(async ()=> {
  const browser = await chromium.launch();
  const page = await browser.newPage();
  await page.goto('https://opensource-demo.orangehrmlive.com/web/index.php/auth/login');
  await page.fill('input[name="username"]', 'Admin');
  await page.fill('input[name="password"]', 'admin123');
  await page.click('button:has-text("Login")');
  await page.waitForNavigation({ waitUntil: 'networkidle' }).catch(()=>{});
  await page.click('a:has-text("PIM")');
  await page.click('a:has-text("Add Employee")');
  await page.waitForSelector('text=Employee Id');
  const label = await page.locator('text=Employee Id').first();
  const labelHTML = await label.evaluate(el => el.outerHTML);
  const wrapper = await label.locator('xpath=ancestor::div[1]').first();
  const wrapperHTML = await wrapper.evaluate(el => el.outerHTML);
  const input = await wrapper.locator('input').first();
  const inputHTML = await input.evaluate(el => el.outerHTML);
  console.log('LABEL HTML:\n', labelHTML);
  console.log('WRAPPER HTML:\n', wrapperHTML);
  console.log('INPUT HTML:\n', inputHTML);
  await browser.close();
})();
